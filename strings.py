course = '''
 
Hi John,
 
I hope this email finds you well.

I want to know your status on ongoing project.

Thanks 

'''

print(course)


# TO WRITE MULTIPLE STRING YOU HAVE TO USE TRIPLE STRING

print(course[23])
print(course[-5])

# WE CAN ALSO PRINT THE INDIVIDUAL CHARACTER BY USING SQUARE BRACKET
# WE CAN ALSO PRINT THE NEGATIVE CHARACTER BY USING - SIGN
# IF YOU WANNA PRINT MULTIPLE CHARACTER USE COLON [:]

print(course[0:23])

name = 'MOHANS'
